package api;

public class ExplorandoMath {

	public static void main(String[] args) {
		// sin, cos, tag, log
		// métodos estáticos, sem a necessidade de instânciar um objeto
		System.out.println(Math.PI);
		System.out.println(Math.pow(2, 3));
		System.out.println(Math.abs(-10.5));
		System.out.println(Math.min(7, 70));
		System.out.println(Math.max(7, 70));
		System.out.println(Math.ceil(6.55)); // arrendondar pra cima
		System.out.println(Math.floor(6.55)); // arredondar pra baixo
		System.out.println(Math.random());
	}
}
